## 一、划水类（Stroke Variations）

### J-stroke（J 形划水）

- **场景**：不换手还要保持直线（独木舟常用，在桨板上用来避免每 3-4 桨就切换手导致节奏紊乱）
- **要点**：桨入水时像普通正向划，拉到脚边时**桨叶向外旋 90°**，末端扫出一个"J"字，靠尾部的横向扫来抵消船头偏转
- **新手误区**：J 画得太早 → 前半段划水都没效率；应该拉到膝盖后才开始扫

### 反手桨（Cross-bow Stroke / Draw Stroke）

- **场景**：需要横向位移（靠岸、抢位、穿过障碍）
- **要点**：桨叶**插入板子侧方（不是前方）**，桨叶与船身平行，**把水往自己身体方向拉**
- **新手误区**：桨柄没扭平 → 桨叶侧面进水变成"刹车"

### 反向划（Back Paddle）

- **场景**：紧急后退、避开前方障碍
- **要点**：桨叶**凹面朝后**，用推的动作把水往前推
- **新手误区**：忘记换桨叶方向 → 桨叶凸面推水会扭板

## 二、转向类（Turn Variations）

### Pivot Turn（支点转向）

- **场景**：**最核心的进阶动作**，可在 2-3 秒内完成 180° 调头（绕标赛必备）
- **要点**：
  1. 后脚踩到板尾翘起来的位置（**T-step**），重心压沉板尾
  2. 板头翘起出水，板子此刻是竖的
  3. 用反手桨（Cross-bow Draw）在板头前方扫一个大弧
- **新手误区**：
  - 重心没沉下去 → 板尾没翘起来 → 转不动
  - 脚站太靠边 → 直接落水
  - **先在浅水区练起板平衡**，摔 10 次是正常的

### Step-back Turn（退步转向）

- **场景**：Pivot Turn 的简化版，不把板头翘起来，只是重心往后移
- **要点**：后脚后退半步到板子中后段，同时大力外划（Sweep Stroke）
- **新手误区**：脚步和桨不同步 → 桨先下水再挪脚

### Sweep Stroke（扫桨转向）

- **场景**：不紧急的缓转，保持前进惯性
- **要点**：桨叶画**大半圆弧**（从板头前方扫到板尾后方），不是直线拉
- **新手误区**：太用力 → 上半身跟着扫过去失去平衡

## 三、平衡与姿态类（Balance & Stance）

### 跪姿 ↔ 站姿切换（Kneeling to Standing）

- **场景**：大浪 / 大风 / 身体疲劳时先跪着缓一缓，状况稳定再站起
- **要点**：
  - 跪 → 站：单膝跪起一脚踩稳→ 重心前移另一脚踩稳 → 缓慢直立
  - 站 → 跪：先蹲下来到"相扑式"深蹲 → 一膝先着板 → 再另一膝
- **新手误区**：直立起身没蹲过渡 → 重心失控摔下水

### 踩浪转向（Surf Stance Turn）

- **场景**：站浪板（Surf SUP）在浪上主动改变方向
- **要点**：脚位改为**前后站**（Surf Stance），身体斜向板身，靠肩膀和髋关节带动板身倾斜
- **新手误区**：仍用平行脚站姿 → 只能手划转向，失去"踩浪"感

### 低姿防风（Low Stance）

- **场景**：阵风或涌浪时
- **要点**：**膝盖弯到 30-45°，身体稍前倾，重心压低**
- **新手误区**：站得笔直紧张 → 一个涌浪就倒

## 四、竞速类（Race Techniques）

### 起桨抓水（Catch Phase）

- **场景**：起步、加速阶段的关键 0.5 秒
- **要点**：
  - 桨叶**尽量远地前探**（上肢前倾，桨柄接近水面）
  - **先让桨叶完全没入水**，再发力拉（用水的重量做阻力，不是用手臂拍水）
- **新手误区**：入水浅 + 立即发力 → 桨叶打滑、划水效率低

### 高频低震动节奏（High-Cadence Rhythm）

- **场景**：中长距离比赛
- **要点**：**桨频 60-70 次/分钟，每桨幅度不要过大**，节奏稳定
- **新手误区**：用力划大幅度 → 心率飙升、几分钟就乏力

### 身体前倾发力链（Body Engagement）

- **场景**：所有划水，但竞速中更关键
- **要点**：**发力顺序是 脚→臀→核心→背阔→手臂**，上半身像个整体板块前倾下压
- **新手误区**：只有手臂在动 → 全身其他部位都是"死重"

## 五、冲浪桨板特有（Surf SUP Only）

### Pop-up（从卧姿到站姿）

- **场景**：不是在平水站姿，而是在浪上低位起身
- **要点**：一个流畅动作从趴姿（Pop-up）或跪姿切换到 Surf Stance
- **新手误区**：起太慢 → 错过浪的推进点；起太快 → 重心不稳

### Bottom Turn（浪底转向）

- **场景**：冲下浪后在浪底改变方向准备上浪
- **要点**：重心压向内侧 Rail（板身边缘），让板子像滑雪板一样"切入"
- **新手误区**：用桨乱拨 → 浪板主要靠**身体倾斜+脚下压板**转向，桨是辅助

### Top Turn（浪顶转向）

- **场景**：冲到浪顶时往回切
- **要点**：重心从后脚切换到前脚，桨叶轻点水面做支点
- **新手误区**：还没到浪顶就开始转 → 失去动能

## 进阶路径建议

- **第一阶段（平水老司机）**：J-stroke、Sweep Stroke、Pivot Turn、跪站切换
- **第二阶段（竞速向）**：高频抓水、全身发力链、反手桨抢位
- **第三阶段（冲浪向）**：Surf Stance、Pop-up、Bottom/Top Turn

**经验值**：把以上动作每个练到"想都不想就能做出"，平均需要 80-100 小时有效下水时间。训练时少求新，多求稳——同一个动作重复 100 次比尝试 10 个新动作收获大得多。

