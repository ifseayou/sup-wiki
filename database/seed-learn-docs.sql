## 四大核心肌群与桨板动作的对应关系

| 肌群 | 代表肌肉 | 桨板中的角色 | 感觉不到它在工作时说明 |
|------|----------|--------------|----------------------|
| **核心** | 腹直肌、腹斜肌、竖脊肌、多裂肌 | 对抗水面晃动、把桨力从上半身传到板面 | 划不到 10 分钟腰就酸 = 核心代偿不足 |
