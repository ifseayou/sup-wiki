## 常见易错点

1. **没备案就直接申请开班**：不行。必须先完成中心备案并通过审核。
2. **教练员等级不匹配**：L5-L6 不是初级教练员就能带，必须确认到中级教练员资质。
3. **开班申请时间太晚**：临近开班才报，容易赶不上审核反馈。
4. **转账截图信息不完整**：看不清金额或付款单位，后续可能被要求补材料。
5. **水域说明写得过于简单**：尤其是自述型水域使用证明，建议把水质、水深、流速、涌浪、面积、位置全部写清。

