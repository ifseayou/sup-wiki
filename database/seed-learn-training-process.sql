## 第一步：中心备案

### 什么时候做

只要你准备开班，就要先做备案。**备案审核通过后，才可以继续申请具体培训班。**

### 备案要提交什么

通过桨板邮箱提交以下材料：

- 俱乐部营业执照
- 俱乐部法人和负责人的姓名、联系方式
- 参与培训教练员的教练员证书
- 水域使用证明

其中，营业执照需要能体现**体育培训经营范围**。

### 水域使用证明怎么理解

水域使用证明可以是以下任一类型：

- 相关行政部门授予水域使用权的盖章证明
- 私人机构授予水域使用权的盖章证明
- 俱乐部出具的盖章情况说明

如果是俱乐部出具情况说明，建议把以下信息写全：

- 水质
- 水深
- 流速
- 涌浪
- 面积
- 地理位置

### 备案后多久有结果

中心收到邮件后会进行审核，**约 10 个工作日**反馈意见。

### 备案阶段执行清单

- 确认营业执照经营范围包含体育培训
- 整理法人和负责人联系人信息
- 核对本次参与培训教练员证书是否齐全
- 准备合规的水域使用证明
- 发送到 `chinasup@sports.cn`

## 第二步：开班申请

### 前提条件

只有**通过中心备案审核**的俱乐部，才可以继续申请具体培训班。

### 什么时候提交

每次培训开班前，**至少提前 1 周**发送申请邮件。

### 邮件标题怎么写

邮件名称按这个格式写：

**xx俱乐部 + 申请办理全国桨板运动水平等级培训班 Lx-Lx**

这里的 `Lx-Lx` 需要写清本次申请覆盖的等级范围。

### 开班申请要附什么材料

- 本次参与培训对应的教练员证书
- 保险证明
- 盖章申请表

### 教练员等级要求

- **L1-L4**：初级教练员即可开展培训
- **L5-L6**：需要中级教练员方可培训

这里要特别注意：附件里的教练员证书，必须和**本次实际参与培训**的教练对应，不能随意替代。

### 申请后多久反馈

中心收到申请后，**约 5 个工作日**反馈意见。

### 开班申请阶段执行清单

- 确认俱乐部已完成备案且审核通过
- 确认培训等级范围写清楚
- 确认带班教练员等级符合要求
- 准备保险证明
- 准备并盖章申请表
- 开班前至少 1 周发送申请邮件

