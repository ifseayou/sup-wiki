## 水域备案标准

可用于开展培训的水域可以是：

- 河流
- 湖泊
- 海洋
- 人工场地

但需要同时满足以下条件：

- 有固定下水区域
- 非航道区域
- 无垂钓
- 不得与其他项目混用

具体标准如下：

| 项目 | 要求 |
|------|------|
| 水质 | 不低于 3 类 |
| 水深 | 平均深度大于 2 米 |
| 流速 | 不超过 0.3 米 / 秒 |
| 涌浪 | 高度不超过 0.2 米 |
| 面积 | 直线长度不小于 250 米，直线宽度不小于 50 米 |

如果你的水域条件接近边界值，建议在提交前先把测量依据、现场照片和说明文字准备完整，避免来回补材料。

